    // merhos to insert values into users table
    public void  insertSignupDetails(){
        String name= txt_username.getText();
        String pwd= txt_password.getText();
        String email= jTextField4.getText();
        String contact= jTextField1.getText();
        
        try{
            
           
            Connection con= DBConnection.getConnection();
            String sql= "insert into users1(name,password,email,contact) values (?,?,?,?)";
            PreparedStatement pst= con.prepareStatement(sql);
            
            pst.setString(1, name);
            pst.setString(2, pwd);
            pst.setString(3, email);
            pst.setString(4, contact);
            
           int updateRowCount =  pst.executeUpdate();
           if(updateRowCount>0){
               JOptionPane.showMessageDialog(this, "Record Inserted Successfully");
               LoginPage page= new LoginPage();
               page.setVisible(true);
               dispose();
           }else{
               JOptionPane.showMessageDialog(this, "Record Insertio Failure");
           }
        
            
        }catch(Exception e){
            e.printStackTrace();
            
        }
    }
    
    
    
    
    //validation
    
    public boolean validateSignup(){
        
         String name= txt_username.getText();
        String pwd= txt_password.getText();
        String email= jTextField4.getText();
        String contact= jTextField1.getText();
        
        
         if(name.equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Username");
            return false;
        }
        if(pwd.equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Password");
            return false;
        }
        
        if(email.equals("") || !email.matches("^.+@.+\\..+$")){
            JOptionPane.showMessageDialog(this, "Please Enter a Valid Email");
            return false;
        }
        
        if(contact.equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Contact Deatils");
            return false;
        }
        
        return true;
    }
    
    
    
    //check duplicate users
    
    public boolean CheckDuplicateUsers(){
        
        String name= txt_username.getText();
        boolean isExists = false;
        
        try{
            Connection con= DBConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("select * from users1S where name= ?");
            pst.setString(1, name);
            ResultSet rs= pst.executeQuery();
            if(rs.next()){
                isExists = true;
            }
            else{
                isExists= false;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "");
        }
        return isExists;
    }

    
    
    
    
    
    
    
    
    
    
